package db.bean;

public class EmploymentBean {
private int num;
private String publish_time;
private String employ_time;
private String employ_address;
private String title;
private String about;
private String detail;
private String link;
public int getNum() {
	return num;
}
public void setNum(int num) {
	this.num = num;
}
public String getPublish_time() {
	return publish_time;
}
public void setPublish_time(String publishTime) {
	publish_time = publishTime;
}
public String getEmploy_time() {
	return employ_time;
}
public void setEmploy_time(String employTime) {
	employ_time = employTime;
}
public String getEmploy_address() {
	return employ_address;
}
public void setEmploy_address(String employAddress) {
	employ_address = employAddress;
}
public String getTitle() {
	return title;
}
public void setTitle(String title) {
	this.title = title;
}
public String getAbout() {
	return about;
}
public void setAbout(String about) {
	this.about = about;
}
public String getDetail() {
	return detail;
}
public void setDetail(String detail) {
	this.detail = detail;
}
public String getLink() {
	return link;
}
public void setLink(String link) {
	this.link = link;
}

}
